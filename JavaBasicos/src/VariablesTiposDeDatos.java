
public class VariablesTiposDeDatos {
	
	public static void main(String [] args) {
		
		// String permite almacenar cadena de texto
		String nombre = "José Manuel";
		int edad = 31;
		
		// char nos permite almacenar un solo caracter
		char inicial = 'C';
		
		// boolean permite almacenar datos true o false
		boolean tienesCrush = true;
		
		// tipos de datos enteros
		
		//short
		short suma = 24 + 104;
		
		//byte
		byte edad1 = 31;
		
		//int
		int numero = 1021321312;
	
		//long
		long numeroLargo = 100_000_000;
		
		//float
		float promedio = 1232.23232f;
		
		//double
		double numeroPi = 3.1415927635627;
		
		System.out.println(numeroLargo);
	}

}
